package myprograms;

public class Nestedfor 
{

	public static void main(String[] args) 
	{
		
        int i,j;
        
        for(i=0;i<3;i++)
        {
        	System.out.println("\n");
        	
        	for(j=0;j<3;j++)
        	{
    			
    			System.out.println(j);
        	}
        }
			
	}

}
